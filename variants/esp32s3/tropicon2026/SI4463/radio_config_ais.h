#define RF_AFC_LIM_3 0x11, 0x20, 0x03, 0x30, 0x01, 0x5C, 0xA0
#define RF_AGC_CTRL 0x11, 0x20, 0x01, 0x35, 0xE0
#define RF_AGC_WIN_12 0x11, 0x20, 0x0C, 0x38, 0x11, 0x11, 0x11, 0x80, 0x1A, 0x20, 0x00, 0x00, 0x28, 0x0C, 0xA4, 0x23
#define RF_RAW_5 0x11, 0x20, 0x05, 0x45, 0x03, 0x00, 0x85, 0x01, 0x00
#define RF_RSSI_4 0x11, 0x20, 0x04, 0x4B, 0x06, 0x09, 0x10, 0x40
#define RF_SEARCH_BAND 0x11, 0x20, 0x02, 0x50, 0x94, 0x0D
#define RF_SPIKE_2 0x11, 0x20, 0x02, 0x54, 0x03, 0x07
#define RF_RSSI_MUTE 0x11, 0x20, 0x01, 0x57, 0x00
#define RF_DSA_5 0x11, 0x20, 0x05, 0x5B, 0x40, 0x04, 0x04, 0x78, 0x20
#define RF_CHFLT1A 0x11, 0x21, 0x0C, 0x00, 0x7E, 0x64, 0x1B, 0xBA, 0x58, 0x0B, 0xDD, 0xCE, 0xD6, 0xE6, 0xF6, 0x00
