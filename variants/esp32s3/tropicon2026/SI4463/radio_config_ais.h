#define RF_CHFLT2 0x11, 0x21, 0x0C, 0x18, 0xDD, 0xCE, 0xD6, 0xE6, 0xF6, 0x00, 0x03, 0x03, 0x15, 0xF0, 0x3F, 0x00
#define RF_PA_TC 0x11, 0x22, 0x01, 0x03, 0x1D
#define RF_SYNTH_7 0x11, 0x23, 0x07, 0x00, 0x2C, 0x0E, 0x0B, 0x04, 0x0C, 0x73, 0x03
// FREQ_CONTROL: MAIANA original — base ~150.9 MHz, step 25 kHz
// AIS A = channel 19 (161.975 MHz), AIS B = channel 21 (162.025 MHz)
#define RF_FREQ_8 0x11, 0x40, 0x08, 0x00, 0x3B, 0x0B, 0x00, 0x00, 0x14, 0x7B, 0x20, 0xFA
#define RF_START_RX 0x32, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00
#define RF_IRCAL 0x17, 0x56, 0x10, 0xCA, 0xF0
